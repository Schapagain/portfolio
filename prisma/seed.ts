import { PrismaClient } from "@prisma/client";
const db = new PrismaClient();

function removeEmptyAttributes(obj) {
  try {
    return Object.fromEntries(
      Object.entries(obj).filter(([_, v]) => v != null && String(v).length > 0)
    );
  } catch (e) {}
  return obj;
}

async function seed() {
  await Promise.all(
    getBooks().map((book) => {
      const filteredBook = removeEmptyAttributes(book);
      if (filteredBook.dateRead) {
        filteredBook.dateRead = new Date(filteredBook.dateRead);
      }
      return db.book.create({ data: filteredBook });
    })
  );
}

